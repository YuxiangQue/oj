    private static int dp(int[][] grid) {

        if (grid[0][0] == 1)
            return 0;

        int[][] dp = new int[grid.length][grid[0].length];

        dp[0][0] = 1;

        for (int i = 1; i < grid.length; ++i) {
            if (grid[i][0] == 1) {
                dp[i][0] = 0;
            } else {
                dp[i][0] = dp[i - 1][0];
            }
        }

        for (int j = 1; j < grid[0].length; j++) {
            if (grid[0][j] == 1) {
                dp[0][j] = 0;
            } else {
                dp[0][j] = dp[0][j - 1];
            }
        }

        for (int i = 1; i < grid.length; i++) {
            for (int j = 1; j < grid[0].length; j++) {
                if (grid[i][j] == 1) {
                    dp[i][j] = 0;
                } else {
                    dp[i][j] = dp[i][j - 1] + dp[i - 1][j];
                }
            }
        }
        return dp[grid.length - 1][grid[0].length - 1];
    }

    private static int dfs(int[][] obstacleGrid, int x, int y, int[][] cache) {
        if (obstacleGrid[x][y] == 1) {
            return 0;
        }
        if (x == 0 && y == 0) {
            cache[x][y] = 1;
            return 1;
        }
        if (x == 0) {
            if (cache[x][y - 1] == -1) {
                cache[x][y - 1] = dfs(obstacleGrid, x, y - 1, cache);
            }
            return cache[x][y - 1];
        }
        if (y == 0) {
            if (cache[x - 1][y] == -1) {
                cache[x - 1][y] = dfs(obstacleGrid, x - 1, y, cache);
            }
            return cache[x - 1][y];
        }

        if (cache[x - 1][y] == -1) {
            cache[x - 1][y] = dfs(obstacleGrid, x - 1, y, cache);
        }
        if (cache[x][y - 1] == -1) {
            cache[x][y - 1] = dfs(obstacleGrid, x, y - 1, cache);
        }
        return cache[x - 1][y] + cache[x][y - 1];
    }

    public static int uniquePathsWithObstacles(int[][] obstacleGrid) {
        int[][] cache = new int[obstacleGrid.length][obstacleGrid[0].length];
        for (int[] cacheRow : cache) {
            Arrays.fill(cacheRow, -1);
        }
        return dfs(obstacleGrid, obstacleGrid.length - 1, obstacleGrid[0].length - 1, cache);
    }

